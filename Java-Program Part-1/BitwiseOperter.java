public class BitwiseOperter {
    public static void main(String args[]){
        System.out.println(5&6);//and    1+1=1   
        System.out.println(5 | 6); //or   0+0=1
        System.out.println(5^6);   //xor
        System.out.println(~5);//compliment
        System.out.println(~0);
        System.out.println(5<<2);  //left shift  (formula   a<<b=a * 2kapowerb)
        System.out.println(6<<1);   //right shift 
    }
}
